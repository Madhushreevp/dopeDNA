# Lists
fruits= ['Apple','Banana','Chikku','Mango','Papaya']
print(fruits)              # printing  the values of list using index starting from to n-1 
print(fruits[3])           # accessing the value of list using index starting from to n 
print(fruits[-1])          # accessing the value of list using index from backside
fruits.append('Jackfruit') # appending new value of list at the end of list
print(fruits)              # updating new value of list at the end of list
print(fruits[2:4])         # slicing and printing the values of list using index starting from index to n-1 index
print(fruits[0:5:2])       # printing a sub-list using stepper as 2
fruits.remove('Banana')    # deleting an item from list using item itself
fruits.pop(4)              # deleting an item from list using index of that item
print(fruits)              # updating and printing the list 
